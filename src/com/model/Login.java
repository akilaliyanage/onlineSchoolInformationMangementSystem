package com.model;

public class Login {

	private String usernameSt;
	private String passwordSt;
	private int empNoSt;
	
	//creating setters and getters
	public String getUsernameSt() {
		return usernameSt;
	}
	public void setUsernameSt(String usernameSt) {
		this.usernameSt = usernameSt;
	}
	public String getPasswordSt() {
		return passwordSt;
	}
	public void setPasswordSt(String passwordSt) {
		this.passwordSt = passwordSt;
	}
	public int getEmpNoSt() {
		return empNoSt;
	}
	public void setEmpNoSt(int empNoSt) {
		this.empNoSt = empNoSt;
	}
	
	
}
